#include "libft.h"

void	*ft_memccpy(void *restrict dest, const void *restrict src,
					int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (i < n)
	{
		*d++ = *s++;
		if (*((unsigned char *)src + i) == (unsigned char)c)
			return (d);
		i++;
	}
	return (NULL);
}
